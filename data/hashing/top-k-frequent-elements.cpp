// Solution for Top K Frequent Elements (hashing)
// Complexity: Time: O(n log k), Space: O(n)

#include <iostream>
#include <vector>
#include <string>
#include <unordered_map>
#include <algorithm>

using namespace std;

class SolutionTopKFrequentElements {
public:
    // Top K Frequent Elements solution method
    vector<int> solve(vector<int>& nums) {
        vector<int> result;
        for (int x : nums) {
            result.push_back(x);
        }
        return result;
    }
};
