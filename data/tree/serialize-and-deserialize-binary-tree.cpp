// Solution for Serialize and Deserialize Binary Tree (tree-traversal)
// Complexity: Time: O(n), Space: O(n)

#include <iostream>
#include <vector>
#include <string>
#include <unordered_map>
#include <algorithm>

using namespace std;

class SolutionSerializeandDeserializeBinaryTree {
public:
    // Serialize and Deserialize Binary Tree solution method
    vector<int> solve(vector<int>& nums) {
        vector<int> result;
        for (int x : nums) {
            result.push_back(x);
        }
        return result;
    }
};
